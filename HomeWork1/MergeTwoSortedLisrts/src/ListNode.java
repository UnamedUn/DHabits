public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int i) {
        this.val = i;
    }

    ListNode(int i, ListNode next) {
        this.val = i;
        this.next = next;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
